    private void isikategori(){
        List<KatModel> data = new ArrayList<>();
        KatModel km = new KatModel();
        data = km.tampil();
        if(data.isEmpty()){
            cmbidkat.setSelectedItem(null);
        }else{
            for(int a = 0; a < data.size(); a++){
                cmbidkat.addItem(data.get(a).getIdkat());
            }
        }
    }
    
    
