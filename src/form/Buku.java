    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        if(txtjudul.getText().isEmpty() || txtpengarang.getText().isEmpty() || txtstok.getText().isEmpty()){
            JOptionPane.showMessageDialog(null," Data belum lengkap !");
        }else{
            bm.setKodebuku(txtkode.getText());
            bm.setJudul(txtjudul.getText());
            bm.setPengarang(txtpengarang.getText());
            bm.setStok(txtstok.getText());
            bm.setIdkat(cmbidkat.getSelectedItem().toString());

            if(btntambah.getText().equalsIgnoreCase("Batal")){
                bm.tambah();
                JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
            }else if(btnedit.getText().equalsIgnoreCase("Batal")){
                bm.edit();
                JOptionPane.showMessageDialog(null,"Data Berhasil DiEdit");
            }
            bersihText();
            kondisiText(false);
            tampilBuku();
            aturTombol();
            btntambah.setText("Tambah");
            btnedit.setText("Edit");
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

