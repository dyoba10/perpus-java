    public void tampilBuku(){
        tbl.getDataVector().removeAllElements();
        tbl.fireTableDataChanged();
        dataBuku.clear();
        dataBuku=bm.tampil();
        for (int i = 0; i < dataBuku.size(); i++) {
            Object[] data=new Object[6];
            data[0]=dataBuku.get(i).getKodebuku();
            data[1]=dataBuku.get(i).getJudul();
            data[2]=dataBuku.get(i).getPengarang();
            data[3]=dataBuku.get(i).getStok();
            data[4]=dataBuku.get(i).getIdkat();
            data[5]=dataBuku.get(i).getNamakat();
            tbl.addRow(data);
            
        }
    }
    public void buatKode(){
        dataBuku = bm.tampil();
        if(dataBuku.isEmpty()){
            txtkode.setText("B-1");
        }else{
            int nourut = Integer.parseInt(dataBuku.get(0).getKodebuku().substring(2))+1;
            txtkode.setText("B-"+nourut);
            txtkode.setEnabled(false); 
        }
    }
